// Function with Parameters
function Add(Num1,Num2){
    console.log("The Sum of the parameters is "+ (Num1+Num2));
}

Add(5,6);

// Function Expression
let Sub = function(a,b){
    console.log("The difference between the Prameters is "+(a-b));
}

Sub(5,6);

// Arrow function
let Mult = (a,b) =>{
    console.log("The product of the Parameters is "+(a*b));
}

Mult(5,6)


